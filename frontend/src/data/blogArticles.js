export const NEW_BADGE_DURATION_DAYS = 3;
const NEW_BADGE_TIMEZONE = "Europe/Paris";

const formatDateInTimezone = (date, timeZone = NEW_BADGE_TIMEZONE) => {
  if (!(date instanceof Date) || Number.isNaN(date.getTime())) return null;
  return new Intl.DateTimeFormat("en-CA", {
    timeZone,
    year: "numeric",
    month: "2-digit",
    day: "2-digit",
  }).format(date);
};

const dateKeyToUtcMs = (dateKey) => {
  if (!dateKey) return Number.NaN;
  const [year, month, day] = dateKey.split("-").map(Number);
  if (!year || !month || !day) return Number.NaN;
  return Date.UTC(year, month - 1, day);
};

export const isArticleNew = (article, now = new Date()) => {
  if (!article?.publishedTime) return false;

  const publishedDate = new Date(article.publishedTime);
  const publishedDateKey = formatDateInTimezone(publishedDate);
  const nowDateKey = formatDateInTimezone(now);

  const publishedUtcMs = dateKeyToUtcMs(publishedDateKey);
  const nowUtcMs = dateKeyToUtcMs(nowDateKey);

  if (Number.isNaN(publishedUtcMs) || Number.isNaN(nowUtcMs)) return false;

  const diffDays = Math.floor((nowUtcMs - publishedUtcMs) / (24 * 60 * 60 * 1000));

  return diffDays >= 0 && diffDays < NEW_BADGE_DURATION_DAYS;
};

// Helper functions
export const getArticleBySlug = (slug) => {
  return BLOG_ARTICLES.find(article => article.slug === slug);
};

export const getFeaturedArticles = () => {
  return BLOG_ARTICLES.filter(article => article.featured);
};

export const getArticlesByCategory = (category) => {
  if (category === 'Tous') return BLOG_ARTICLES;
  return BLOG_ARTICLES.filter(article => article.category === category);
};

export const getRelatedArticles = (currentArticleId, limit = 3) => {
  const currentArticle = BLOG_ARTICLES.find(a => a.id === currentArticleId);
  if (!currentArticle) return [];
  
  return BLOG_ARTICLES
    .filter(a => a.id !== currentArticleId && a.category === currentArticle.category)
    .slice(0, limit);
};

